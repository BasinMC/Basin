package org.basinmc.faucet.command.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;
import java.util.concurrent.TimeUnit;

/**
 * Denotes the parsing system for a given parameter as converting a date string in any format into
 * a number of any given time unit.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.PARAMETER)
public @interface DateFormat {

  TimeUnit value() default TimeUnit.SECONDS;
}
